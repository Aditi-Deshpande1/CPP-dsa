#include<iostream>
using namespace std;
 int main ()
 {
    int age = 18;
    cout << "Age : " << age << endl;
    cout<<sizeof(age)<<endl;
    char grade ='A';
    cout<<"Grade : "<<grade<<endl;
    cout<<sizeof(grade)<<endl;
    float PI = 3.14;
    cout<<PI<<endl;
    cout<<sizeof(PI)<<endl;
    bool isCodingFun = true;
    cout<<"Is Coding Fun : "<<isCodingFun<<endl;
    cout<<sizeof(isCodingFun)<<endl;
    return 0;
 }
